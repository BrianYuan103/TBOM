    private static void generateLang(List<String> list) throws IOException {
        for (String str : list) {
            String lang = "\"item.bindingofminecraft." + str + "_item\"";
            System.out.println("  " + lang + ": \"" + str.replace("collectibles_", "").split("_")[1] + "\",");
        }
    }

    private static void generateJson(List<String> list) throws IOException {
        for (String str : list) {
            BufferedWriter bufferedWriter = new BufferedWriter(new FileWriter(new File(
                    "./auto_generated/json/" + str + "_item" + ".json")));
            bufferedWriter.write("{\n");
            bufferedWriter.write("\t\"parent\": \"item/generated\",\n");
            bufferedWriter.write("\t\"textures\": {\n");
            bufferedWriter.write("\t\t\"layer0\": \"bindingofminecraft:items/collectibles/" + str + "\"\n");
            bufferedWriter.write("\t}\n");
            bufferedWriter.write("}\n");
            bufferedWriter.close();

        }
    }

